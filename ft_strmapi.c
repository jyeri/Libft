#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*new;
	unsigned int	i;
	int				x;

	if (!s || !f)
		return (NULL);
	x = 0;
	while (s[x] != '\0')
		x++;
	new = (char *)malloc(sizeof(char) * x + 1);
	if (!new)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
